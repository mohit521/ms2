public class Swap {
    public static void main(String[] args) {
        
        int a=10;
        int b=20;
        System.out.println("Before the swap "+a+" "+b);
      int c=a;
         c=a;
         a=b;
         b=c;
        System.out.println("After the swap  "+a+" "+b);
    
}
}